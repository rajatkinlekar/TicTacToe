class Cell {
    
    constructor(id, option) {
        this.id = id;
        this.option = option;
    }

    getId() {
        return this.id;
    }

    getOption() {
        return this.option;
    }

    setId(id) {
        this.id = id;
    }

    setOption(option) {
        this.option = option;
    }

}